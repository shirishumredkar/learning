
public class AssignmentOper {

	public static void main(String[] args) {
		int a = 5;
		a += 4;
		System.out.println("Value of a is "+a); // a = a + 4
		a = 5;
		a -= 4;
		System.out.println("Value of a is "+a); // a = a - 4
		a = 5;
		a *= 4;
		System.out.println("Value of a is "+a); // a = a*4
		a = 5;
		a /= 4;
		System.out.println("Value of a is "+a); // a = a/4
		a = 5;
		a %= 4;
		System.out.println("Value of a is "+a); // a = a%4
		

	}

}
