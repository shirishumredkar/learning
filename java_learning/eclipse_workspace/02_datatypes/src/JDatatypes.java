
public class JDatatypes {

	public static void main(String[] args) {
		String str = new String("Shirish");
		
		int    age=36;
		byte   meal=3;
		short  height=5;
		long   weight=60;
		
		float  income=120000.345f;
		double annual=120000.045678899999d;
		
		boolean bool = true;
		char    chr  = 'S';
				
		System.out.println("My name is "+str);
		System.out.println("My age is "+age);
		System.out.println("My meal is "+meal);
		System.out.println("My height is "+height);
		System.out.println("My weight is "+weight);
		
		System.out.println("My income is "+income);
		System.out.println("My annual income is "+annual);
		
		System.out.println("Boolean is "+bool);
		System.out.println("My name Starts with "+chr);
	}

}
